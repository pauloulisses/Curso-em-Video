/*
1. document.querySelector(".p"): Procura o primeiro elemento com a classe "p" no HTML.

2. const paragrafo: Cria uma variável chamada paragrafo para guardar esse elemento.

3. paragrafo.textContent: Acessa o texto existente dentro do parágrafo.

4. += " JavaScript!": Adiciona (concatena) o texto " JavaScript!" ao final do texto que já está lá.
*/


const paragrafo = document.querySelector(".p");
paragrafo.textContent += " JavaScript!";



